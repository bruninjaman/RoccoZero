namespace O9K.Evader.Abilities.Heroes.NyxAssassin.Impale;

using Base;
using Base.Evadable;

using Core.Entities.Abilities.Base;
using Core.Entities.Units;

using Divine.Modifier.Modifiers;

using Metadata;

using Pathfinder.Obstacles.Modifiers;

internal sealed class ImpaleEvadable : LinearProjectileEvadable, IModifierCounter
{
    public ImpaleEvadable(Ability9 ability, IPathfinder pathfinder, IMainMenu menu)
        : base(ability, pathfinder, menu)
    {
        this.Blinks.UnionWith(Abilities.Blink);

        this.Disables.UnionWith(Abilities.Disable);

        this.Counters.Add(Abilities.Mischief);
        // Counters.Add(Abilities.SleightOfFist); //todo invul time check
        this.Counters.Add(Abilities.BallLightning);
        this.Counters.Add(Abilities.AttributeShift);
        this.Counters.Add(Abilities.Spoink);
        this.Counters.UnionWith(Abilities.StrongShield);
        this.Counters.UnionWith(Abilities.Invulnerability);
        this.Counters.UnionWith(Abilities.MagicShield);
        this.Counters.UnionWith(Abilities.Heal);
        this.Counters.Add(Abilities.Armlet);
        this.Counters.UnionWith(Abilities.Suicide);
        this.Counters.UnionWith(Abilities.SlowHeal);
        this.Counters.Add(Abilities.BladeMail);
        this.Counters.Add(Abilities.Bulwark);

        this.ModifierCounters.UnionWith(Abilities.AllyStrongPurge);
        this.ModifierCounters.UnionWith(Abilities.Invulnerability);
        this.ModifierCounters.UnionWith(Abilities.StrongMagicShield);
    }

    public bool ModifierAllyCounter { get; } = true;

    public bool ModifierEnemyCounter { get; } = false;

    public void AddModifier(Modifier modifier, Unit9 modifierOwner)
    {
        var obstacle = new ModifierAllyObstacle(this, modifier, modifierOwner);
        this.Pathfinder.AddObstacle(obstacle);
    }

    protected override void AddObstacle()
    {
        if (this.Owner.HasModifier("modifier_nyx_assassin_burrow"))
        {
            return;
        }

        base.AddObstacle();
    }
}