        public static HeroId GetHeroId(this AbilityId id)
        {
            if (ids.TryGetValue(id, out var heroId))
            {
                return heroId;
            }

            return HeroId.npc_dota_hero_base;
        }

        public static CUnit GetUnit(this AbilityId id)
        {
            return UnitManager<CUnit>.Units.ToList().Find(x => x.Abilities.Any(a => a.Id == id))?.Owner;
        }

        public static CUnit GetUnit<TTeam>(this AbilityId id)
            where TTeam : IType
        {
            return UnitManager<CUnit, TTeam>.Units.ToList().Find(x => x.Abilities.Any(a => a.Id == id))?.Owner;
        }

        public static CAbility GetAbility(this AbilityId id)
        {
            return AbilityManager.Abilities.ToList().Find(x => x.Id == id);
        }

